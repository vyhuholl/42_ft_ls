#include "ft_ls.h"

int ft_max(int x, int y)
{
    if (x > y)
        return (x);
    return (y);
}

int ft_nbrlen(int n)
{
    int i;

    i = 1;
    while (n >= 10)
    {
        n /= 10;
        i++;
    }
    return (i);
}
